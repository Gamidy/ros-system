    heating_w = btu_to_w(entry.get("制热量"))
    if not heating_w:
        heating_w = entry.get("制热量")
        if isinstance(heating_w, (int, float)):
            pass
        else:
            heating_w = None

    return {
        "brand": entry["品牌"],
        "model": entry["型号"],
        "market": entry["市场"],
        "product_type": "分体式空调",
        "cooling_capacity_w": cooling_w,
        "heating_capacity_w": heating_w,
        "energy_rating": entry.get("能效等级"),
        "noise_indoor_db": float(entry["室内噪音"]) if entry.get("室内噪音") else None,
        "noise_outdoor_db": float(entry["室外噪音"]) if entry.get("室外噪音") else None,
        "factory_price": entry.get("价格"),
        "launch_year": entry.get("上市年份"),
        "notes": f"AI采集-{entry['市场']}市场",
    }

