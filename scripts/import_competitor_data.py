def btu_to_w(val):
    """BTU/h → Watt 转换（纯数字视为已为W，不转）"""
    if val is None:
        return None
    try:
        s = str(val)
        if "btu" in s.lower():
            v = float(s.lower().replace("btu/h","").replace("btu","").replace(",","").strip())
            return int(v * 0.29307107)
        return int(float(s))
    except (ValueError, TypeError):
        return None

